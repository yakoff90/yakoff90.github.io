    var networkHealth = 1.0; // –ü–æ–∫–∞–∑–Ω–∏–∫ "–∑–¥–æ—Ä–æ–≤'—è" –º–µ—Ä–µ–∂—ñ (1.0 = –¥–æ–±—Ä–µ, 0.3 = –ø–æ–≥–∞–Ω–æ).

