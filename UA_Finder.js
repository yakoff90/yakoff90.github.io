    // 2. –ú–∏—Ç—Ç—î–≤–æ –≤—ñ–∑—É–∞–ª—å–Ω–æ –ø—Ä–∏–±–∏—Ä–∞—î–º–æ —Å—Ç–∞—Ä—ñ –º—ñ—Ç–∫–∏ (—á–µ—Ä–µ–∑ –ø–æ–¥—ñ—é)
    try{ document.dispatchEvent(new CustomEvent('ltf:settings-changed',{detail:{...st}})); }catch(e){}
    
